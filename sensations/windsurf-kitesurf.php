	  var vitesses1 = [];
	  var vitesses2 = [];
	  
		$(document).ready(function($) {
			$.ajax({
				url: "https://spreadsheets.google.com/feeds/list/1eCnnsOdcwRKJ_kpx1uS-XXJoJGFSvm3l3ez2K9PpPv4/default/public/values?alt=json",
				type: 'GET',
				crossDomain: true,
				dataType: 'json'
			}).then(function(data) {
				vitesses1 = [];
				vitesses2 = [];
				var ligne, d, v100, vmax, i, pratique, distanceFoil, distanceFoilTotale;
				var v = [];
				distanceFoilTotale = 0.0;
				vitesses1[0] = ['Date', 'V 100m', 'V Max'];
				vitesses2[0] = ['Date', 'V 100m', 'V Max'];
				var j1 = 1;
				var j2 = 1;
				for (i=0; i < data.feed.entry.length; i++) {
					ligne = data.feed.entry[i];
					d = ligne.gsx$date.$t;
					pratique = ligne.gsx$pratique.$t; 
					vmax = ligne.gsx$vmaxk72noeuds.$t;
					v100 = ligne.gsx$v100mk72.$t;
					distanceFoil = ligne.gsx$distancekm.$t;
					if (distanceFoil == "") {
						distanceFoil = 0.0;
					}
					distanceFoil = parseFloat(distanceFoil);
					if (pratique == "Windfoil") {
						distanceFoilTotale = distanceFoilTotale + distanceFoil;
					}
					if (vmax != '') {
						v = [];
						v[0] = new Date(d);
						v[1] = parseFloat(v100);
						v[2] = parseFloat(vmax);
						if (pratique == 'Windsurf') {
							vitesses1[j1] = v;
							j1 = j1 + 1;
						}
						else {
							vitesses2[j2] = v;
							j2 = j2 + 1;
						}
					}
				}
				jQuery("#distance-foil").html(Math.round(distanceFoilTotale).toString());
				drawChart1();
				drawChart2();
			});
		});
	  	  
